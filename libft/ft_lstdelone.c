#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!lst || !del)
		return ;
	while (lst)
	{
		del(lst->content);
		tmp = lst;
		free(tmp);
		lst = NULL;
	}
}
